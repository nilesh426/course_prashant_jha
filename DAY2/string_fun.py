s = "Python is a High level programming language"

print(s.lower()) #python is a high level programming language
print(s.upper()) #PYTHON IS A HIGH LEVEL PROGRAMMING LANGUAGE
print(s.swapcase()) #pYTHON IS A hIGH LEVEL PROGRAMMING LANGUAGE (upper->lower and lower->upper)
print(s.title()) #Python Is A High Level Programming Language (All first letters of words are capitalized)
print(s.capitalize()) #Python is a high level programming language (only first letter of the first word is capital)